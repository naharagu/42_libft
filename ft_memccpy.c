#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t	i;

	i = -1;
	while (++i < n)
	{
		((unsigned char *)dst)[i] = ((const unsigned char *)src)[i];
		if (((unsigned char *)src)[i] == (const unsigned char)c)
			return (dst + i + 1);
	}
	return (NULL);
}
